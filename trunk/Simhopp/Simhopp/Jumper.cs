using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Simhopp
{
    public class Jumper
    {
        private int ID;
        private string name;

        //konstruktor
        public Jumper()
        {
            this.ID = -1;
            this.name = "";
        }

        public Jumper(int ID, string name)
        {
            this.ID = ID;
            this.name = name;
        }

